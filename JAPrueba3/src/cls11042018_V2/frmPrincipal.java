    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        lstPersonas.add(new Persona(this.jtxtNombres.getText(), this.jtxtApellidos.getText(), this.jtxtUsuario.getText(), this.jtxtContrasenia.getText()));
        this.jtxtApellidos.setText("");
        this.jtxtNombres.setText("");
        actualizarTabla();
        
        btnNuevo.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblPersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPersonasMouseClicked
        // TODO add your handling code here:
        if (this.tblPersonas.getRowCount() > 0){
            int indice = this.tblPersonas.getSelectedRow();
            tmp= lstPersonas.get(indice);
            this.jtxtNombres.setText(tmp.nombres);
            this.jtxtApellidos.setText(tmp.apellidos);
            esNuevo = false;
            btnEditar.setEnabled(true);
            btnEliminar.setEnabled(true);
            btnCancelar.setEnabled(true);
            btnNuevo.setEnabled(false);
            btnGuardar.setEnabled(false);
        }
    }//GEN-LAST:event_tblPersonasMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        tmp.nombres = jtxtNombres.getText();
        tmp.apellidos = jtxtApellidos.getText();
        tmp.contrasenia = this.jtxtContrasenia.getText();
        tmp.usuario = this.jtxtUsuario.getText();
        actualizarTabla();
        
        btnNuevo.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        this.jtxtApellidos.setText("");
        this.jtxtNombres.setText("");
        this.tblPersonas.clearSelection();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        lstPersonas.remove(tmp);
        actualizarTabla();
        this.jtxtApellidos.setText("");
        this.jtxtNombres.setText("");
        btnNuevo.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnCancelar.setEnabled(false);
        this.tblPersonas.clearSelection();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        // TODO add your handling code here:
        btnGuardar.setEnabled(true);
        btnNuevo.setEnabled(false);
        btnCancelar.setEnabled(true);
        esNuevo = true;
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        
        if (esNuevo){
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnCancelar.setEnabled(false);
        }else{
            btnNuevo.setEnabled(true);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnCancelar.setEnabled(false);
            this.jtxtNombres.setText("");
            this.jtxtApellidos.setText("");
            this.tblPersonas.clearSelection();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

